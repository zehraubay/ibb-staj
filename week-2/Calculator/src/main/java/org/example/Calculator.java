package org.example;

public class Calculator {
    public int plus(int sayi1, int sayi2) {
        return sayi1+sayi2;
    }

    public int minus(int sayi1, int sayi2) {
        return sayi1-sayi2;
    }

    public int times(int sayi1, int sayi2) {
        return sayi1*sayi2;
    }

    public int dividedby(int sayi1, int sayi2) {
        return sayi1/sayi2;
    }


}
